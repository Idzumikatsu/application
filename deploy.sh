    log_warning "–§–∞–π–ª docker-compose.yml –±—ã–ª –∏–∑–º–µ–Ω–µ–Ω, –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω–µ–Ω–∞ –ø–æ–ª–Ω–∞—è –ø–µ—Ä–µ—Å–±–æ—Ä–∫–∞"
